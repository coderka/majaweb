A website template is basically a premade webpage — or set of 
webpages — that can be customized with images, videos and other 
style elements to create a complete website.

Often created by web developers, the template includes 
everything needed (i.e. HTML, CSS, and any other code required) 
for it to function.

<em>How to build a website using templates</em>

The website design process is greatly simplified when you pair 
a template with a popular web template system like WordPress 
or Joomla. Instead of having to understand the technical 
aspects of web design, most of the setup is practically 
automated: with the click of a button you can install 
website templates, called “themes,” then customize them 
to your liking without having to write a single line of code.

There are also many plugins and apps available 
on the Internet that allow you to add additional 
functionality to your website, like improving 
search engine optimization (SEO), boosting your 
social media marketing, or strengthening the 
overall security of your blog.

Before you start randomly downloading great-looking 
web templates, it is important to note that you need 
more than a template to build a website: you need a 
place to put it.

<em>Here’s how to get started:</em>

1.Register your domain name.
2.Find a webhosting service. While you can 
also choose a different provider, you can 
often purchase webhosting through the same 
company as your domain name.
3.Install a content management system (CMS) 
like WordPress or Joomla on your webhost’s server. 
Many webhosts offer several CMSs to choose from, 
with easy DIY setup.
4.Find, download and install a website template. 
There are many templates available for free, 
or you can purchase one for a fee; 
choose the template that fits your particular needs and budget.
5.Replace generic content and logos with your own 
images and words.
6.Install additional features like shopping carts, 
galleries, search engine optimization plugins and other apps.
